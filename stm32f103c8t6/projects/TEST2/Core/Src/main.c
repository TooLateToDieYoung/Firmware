static void App_Init(void)
{
	// hc05
	app.hc05.device = HC05_Constructor(USART1);
	app.hc05.tx = Buffer_Constructor(50);
	Printf((uint8_t*)"test\r\n", 6);
	
	// vl53l1x
	app.vl53l1x.device = 0x52;
	app.vl53l1x.check = 0;
	app.vl53l1x.distance = 0;
	app.vl53l1x.isTriggered = False;
	
	LL_I2C_Enable(I2C1);
	
	LL_mDelay(200);
	
	VL53L1X_SensorInit(app.vl53l1x.device);
	VL53L1X_StartRanging(app.vl53l1x.device); 
	
	LL_TIM_EnableIT_UPDATE(TIM4);
	LL_TIM_EnableCounter(TIM4);
}

static void App_Task(void)
{
	if( !app.vl53l1x.isTriggered ) return;

	app.vl53l1x.check = 0;
	VL53L1X_CheckForDataReady(app.vl53l1x.device, &app.vl53l1x.check);
	
	LL_GPIO_TogglePin(TEST_GPIO_Port, TEST_Pin);
	
	if( app.vl53l1x.check == 1 ) {
		while( LL_USART_IsEnabledIT_TXE(app.hc05.device->USARTx) ) { }
		Printf((uint8_t*)"check ok!\r\n", 11);
		LL_GPIO_SetOutputPin(TEST_GPIO_Port, TEST_Pin);
		
		app.vl53l1x.distance = 0;
		VL53L1X_GetDistance(app.vl53l1x.device, &app.vl53l1x.distance);
		if( app.vl53l1x.distance != 0 ) { 
			while( LL_USART_IsEnabledIT_TXE(app.hc05.device->USARTx) ) { }
			Printf((uint8_t*)"fetch ok!\r\n", 11);
			LL_GPIO_ResetOutputPin(TEST_GPIO_Port, TEST_Pin);
		}
	}
	
	app.vl53l1x.isTriggered = False;
}

